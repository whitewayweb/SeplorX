#!/usr/bin/env node

const fs = require("fs");
const path = require("path");
const { compile } = require("json-schema-to-typescript");

/**
 * Smart WooCommerce Type Generator
 * 
 * Instead of relying on static (and often outdated) OpenAPI files,
 * this script uses the WordPress REST API's own discovery features.
 * 
 * By making an OPTIONS request to the live endpoints, we get the exact
 * schema used by the store, including any plugin-added custom fields.
 */

const OUT_DIR = path.join(__dirname, "../src/lib/channels/woocommerce/api/types");
if (!fs.existsSync(OUT_DIR)) {
  fs.mkdirSync(OUT_DIR, { recursive: true });
}

const ENDPOINTS = [
  { name: "WCProduct",   path: "/wp-json/wc/v3/products" },
  { name: "WCVariation", path: "/wp-json/wc/v3/products/variations" }, // Usually shared with products but sometimes distinct
  { name: "WCOrder",     path: "/wp-json/wc/v3/orders" },
  { name: "WCCustomer",  path: "/wp-json/wc/v3/customers" },
  { name: "WCCategory",  path: "/wp-json/wc/v3/products/categories" },
];

// Load .env.local if it exists to find WOO_STORE_URL
const envLocalPath = path.join(__dirname, "../.env.local");
if (fs.existsSync(envLocalPath)) {
  const envContent = fs.readFileSync(envLocalPath, "utf8");
  const match = envContent.match(/^WOO_STORE_URL=(.*)$/m);
  if (match) process.env.WOO_STORE_URL = match[1].trim();
}

async function main() {
  const storeUrl = process.argv[2] ?? process.env.WOO_STORE_URL;

  if (!storeUrl) {
    console.warn("⚠️  No store URL provided. Usage: yarn generate:woo-types <store_url>");
    console.warn("   You can also set WOO_STORE_URL in your .env.local file.");
    // If no store is provided, we just exit gracefully.
    process.exit(0);
  }

  const cleanUrl = storeUrl.replace(/\/$/, "");

  console.log(`🚀 Generating WooCommerce types from ${cleanUrl}...\n`);

  for (const { name, path: apiPath } of ENDPOINTS) {
    const url = `${cleanUrl}${apiPath}`;
    console.log(`-> Fetching schema for ${name}...`);

    try {
      const res = await fetch(url, { method: "OPTIONS" });
      if (!res.ok) {
        console.error(`   [X] Failed: ${res.status} ${res.statusText}`);
        continue;
      }

      const discovery = await res.json();
      const schema = discovery.schema;

      if (!schema) {
        console.error(`   [X] No schema found in OPTIONS response for ${name}`);
        continue;
      }

      // JSON Schema Cleanup for WordPress oddities
      // 1. WP often omits "object" type at root
      if (!schema.type) schema.type = "object";
      
      // 2. Remove 'context' which is WP-specific metadata
      if (schema.properties) {
        Object.keys(schema.properties).forEach(key => {
          if (schema.properties[key].context) {
            delete schema.properties[key].context;
          }
        });
      }

      const ts = await compile(schema, name, {
        bannerComment: `/* eslint-disable */\n/**\n * This file was automatically generated by json-schema-to-typescript.\n * DO NOT MODIFY IT BY HAND. Instead, change the source JSONSchema file,\n * and run generate-woocommerce-types.js to regenerate this file.\n */`,
        style: { singleQuote: true, semi: true },
        unreachableDefinitions: true
      });

      const outPath = path.join(OUT_DIR, `${name.toLowerCase()}Schema.ts`);
      fs.writeFileSync(outPath, ts);
      console.log(`   ✅ Generated ${path.basename(outPath)}`);

    } catch (err) {
      console.error(`   [X] Error: ${err.message}`);
    }
  }

  console.log(`\n✨ Done! Types generated in ${OUT_DIR}`);
}

main().catch(err => {
  console.error("Fatal error:", err);
  process.exit(1);
});
